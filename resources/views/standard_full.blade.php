   <h4>Day 13 (Thu, 31.10.19): Memphis, Sakkara, Giza Pyramids & Sphinx </h4>
   <p>

  	 After breakfast, meet your private Egyptologist guide for a tour to Giza plateau, site of <a href="http://www.misrtravel.net/Travel-Guide-of-Egypt/egypy-guide/Pyramids-Of-Giza-Attractions">the legendary Pyramids</a>  and <a href="http://www.misrtravel.net/Travel-Guide-of-Egypt/egypy-guide/Sphinx-of-Giza">iconic Sphinx</a> figure. Visit the great Pyramids of Cheops, Chephren & Mykerinus and marvel at these incredible architectural feats that still puzzle archaeologists. Then proceed to visit the Great Sphinx and the Valley temple, where the mummification takes place before burial. 
  	 Leaving the Pyramids behind, travel on to the necropolis of Saqqara, that brims over with sand strewn temples, ancient tombs and the site of <a href="http://www.misrtravel.net/Travel-Guide-of-Egypt/egypy-guide/Pyramid-of-Djoser-Saqqara">Djoser’s step Pyramid</a>  constructed by Imhotep, the Pharaoh’s chief architect in 2700 BC. Stop for lunch at a local restaurant, before heading to Memphis, the capital of ancient Egypt during the old kingdom. It was considered to be a centre of rule and culture for over 3000 years and see the statue of Ramses II before returning to the hotel.
   </p>
  	<br>

   <p><strong>Meals:</strong> Breakfast,	</p>


