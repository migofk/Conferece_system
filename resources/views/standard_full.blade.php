  Stop for lunch at a quality local restaurant, before driving  to visit <a href="http://www.misrtravel.net/Travel-Guide-of-Egypt/egypy-guide/The-Egyptian-Museum-of-Antiquities-Attractions">the Egyptian museum</a>  , which features stunning artifacts and a rare collection of 5000 years of  art from the Pharaonic period, including some of the riches of Tutankhamen's gold jewelry and coffin, discovered in his intact tomb in <a href="http://www.misrtravel.net/Travel-Guide-of-Egypt/egypy-guide/Valley-kings-luxor-thebes">the Valley of Kings</a>  in 1920.
  Then driving to Saladin citadel, and exploring the Alabaster mosque of Muhammad Ali Pasha. Proceed to Islamic Cairo, stroll into Khan el Khalili bazaar, the most exciting souks that have hardly changed for centuries.Back to the hotel.
